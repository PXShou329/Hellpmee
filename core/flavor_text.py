_SIMPLIFIED_COMMON = set("过这点说时间国对开关电话东车书闷觉乐爱给问题应该")

def is_valid_flavor_text(text: str, item: str) -> bool:
    """
    檢查小語是否合格（用於 AI 生成結果驗證）。
    """
    if not text:
        return False
    t = text.strip()
    if len(t) < 8:
        return False
    if len(t) > 60:
        return False
    # 不以標點殘尾結尾
    if t[-1] in "，、：,:":
        return False
    # 不含明顯殘句
    if re.search(r"牢大[，,]\s*$", t):
        return False
    if t.endswith("這") or t.endswith("那") or t.endswith("的"):
        return False
    # 簡體常見字
    if any(ch in _SIMPLIFIED_COMMON for ch in t):
        return False
    # 必須提到 item（或至少含「」引號內容）
    # item 可能很長（含店家），取核心比對
    core = item.split("（")[0].strip()
    if core and core not in t and "「" not in t:
        return False
    return True


def pick_food_text(item: str) -> str:
    """從食物模板池抽一句合格的"""
    for _ in range(5):
        tpl = random.choice(FOOD_TEMPLATES)
        text = tpl.format(item=item)
        if is_valid_flavor_text(text, item):
            return text
    # 最終保底
    return f"牢大，今天就吃「{item}」，本小姐已經幫你決定了。"


def pick_drink_text(item: str) -> str:
    """從飲料模板池抽一句合格的（依飲品類型選池）"""
    core = item.split("（")[0]
    pool = list(DRINK_TEMPLATES)
    if any(h in core for h in _HAPPY_HINT):
        pool += DRINK_TEMPLATES_HAPPY * 2   # 加權
    elif any(h in core for h in _HEALTHY_HINT):
        pool += DRINK_TEMPLATES_HEALTHY * 2
    for _ in range(5):
        tpl = random.choice(pool)
        text = tpl.format(item=item)
        if is_valid_flavor_text(text, item):
            return text
    return f"牢大，今天就喝「{item}」，本小姐已經幫你選好了。"


